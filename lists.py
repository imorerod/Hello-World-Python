
# can put strings, numbers or booleans
friends = ['Morgan', 'Miguel', 'Tyler', 'Ryan', 'Jacob']

# index 2 of friends list/array
print(friends[2])

# starts indexing at -1 (NOT 0) from the end of the list
print(friends[-2])

# prints value at index 1 and greater
print(friends[1:])

# prints value at index 1 & 2 (DOES NOT INCLUDE 3)
print(friends[1:3])

# modify value at index 4
friends[4] = 'Bob'

